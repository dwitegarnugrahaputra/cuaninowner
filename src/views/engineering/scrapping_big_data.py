def calculate_svg_geometry_path(price_history):
    min_p, max_p = min(price_history), max(price_history)
    price_range = (max_p - min_p) if max_p != min_p else 1

    y_coordinates = []
    for price in price_history:
        # Algoritma normalisasi koordinat sumbu Y ke dalam viewport box grafik dashboard (0-160px)
        calculated_y = 130 - ((price - min_p) / price_range * 90)
        y_coordinates.append(calculated_y)

    # Mengembalikan string format d path SVG: M (Move To), Q (Quadratic Bezier Curve), T (Smooth Extension)
    return f"M 30 {y_coordinates[0]:.0f} Q 180 {y_coordinates[1]:.0f} 340 {y_coordinates[2]:.0f} T 650 {y_coordinates[3]:.0f}"

